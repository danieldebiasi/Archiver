    private void jButtonCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCriarActionPerformed
        
        if(jTextFieldDiretorio.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Selecione um diretório!", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else if(jTextFieldNomeArchive.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Dê um nome ao Archive!", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else{
            Archive archive = new Archive();
            
            if(archive.criar(jTextFieldDiretorio.getText(), jTextFieldNomeArchive.getText())){
                for(int i = 0; i < jTableArquivosNovoArchive.getRowCount(); i++){
                    archive.inserir(jTextFieldDiretorio.getText()+File.separator+jTextFieldNomeArchive.getText().concat(".sky"), 
                                    jTableArquivosNovoArchive.getModel().getValueAt(i, 0).toString());
                }
                JOptionPane.showMessageDialog(null, "Archive criado com sucesso!", "Novo Archive", JOptionPane.INFORMATION_MESSAGE);
                
                jTextFieldDiretorio.setText("");
                jTextFieldNomeArchive.setText("");
                jTextFieldNomeArchive.transferFocusBackward();
                DefaultTableModel model = (DefaultTableModel)jTableArquivosNovoArchive.getModel();
                model.setRowCount(0);
            }else{
                JOptionPane.showMessageDialog(null, "Erro na criação do Archive.\nJá existe um Archive com o mesmo nome no diretório especificado.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonCriarActionPerformed

    private void jButtonExtrairSelecionadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExtrairSelecionadoActionPerformed
        Archive archive = new Archive(jTextFieldArchive.getText());
        TelaSelecao sel = new TelaSelecao(this, true);
        if(!jTableArquivos.getSelectionModel().isSelectionEmpty()){
            String dir = sel.showDialog(JFileChooser.DIRECTORIES_ONLY, false);
            if(dir != null){
                if(archive.extrair(jTableArquivos.getValueAt(jTableArquivos.getSelectedRow(), 0).toString(), dir)){
                    JOptionPane.showMessageDialog(null, "Arquivo extraído com sucesso!", "Extrair Selecionado", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Erro na extração do arquivo!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um arquivo da lista!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonExtrairSelecionadoActionPerformed

    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        Archive archive = new Archive();
        if(jTextFieldArchive.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Selecione um archive!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }else if(jTextFieldArquivo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Selecione um arquivo!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }else if(!archive.inserir(jTextFieldArchive.getText(), jTextFieldArquivo.getText())){
            JOptionPane.showMessageDialog(null, "Arquivo excede tamanho máximo!\nMáximo: 1GB", "Erro", JOptionPane.ERROR_MESSAGE);
        }else{
            jTextFieldArquivo.setText("");
            updateTableArquivos();   
        }
    }//GEN-LAST:event_jButtonInserirActionPerformed

    private void jButtonExtrairTudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExtrairTudoActionPerformed
        Archive archive = new Archive(jTextFieldArchive.getText());
        TelaSelecao sel = new TelaSelecao(this, true);
        if(jTableArquivos.getRowCount() != 0){
            String dir = sel.showDialog(JFileChooser.DIRECTORIES_ONLY, false);
            if(dir != null){
                if(archive.extrairTudo(dir)){
                   JOptionPane.showMessageDialog(null, "Arquivos extraídos com sucesso!", "Extrair Tudo", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Erro na extração dos arquivos!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Archive vazio!\nNada a extrair.", "Extrair Tudo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonExtrairTudoActionPerformed

    private void jButtonRemoverSelecionadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverSelecionadoActionPerformed
        Archive archive = new Archive(jTextFieldArchive.getText());  
        if(!jTableArquivos.getSelectionModel().isSelectionEmpty()){
            if(archive.remover(jTableArquivos.getValueAt(jTableArquivos.getSelectedRow(), 0).toString())){
                JOptionPane.showMessageDialog(null, "Arquivo removido com sucesso!", "Remover Selecionado", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Erro na remoção do arquivo!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            updateTableArquivos();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um arquivo da lista!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonRemoverSelecionadoActionPerformed

    private void jButtonRemoverTudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverTudoActionPerformed
        Archive archive = new Archive(jTextFieldArchive.getText());
        if(jTableArquivos.getRowCount() != 0){
            if(archive.removerTudo()){
                JOptionPane.showMessageDialog(null, "Arquivos removidos com sucesso!", "Remover Tudo", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Erro na remoção dos arquivos!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            updateTableArquivos();
        }else{
            JOptionPane.showMessageDialog(null, "Archive vazio!\nNada a remover.", "Extrair Tudo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonRemoverTudoActionPerformed

